MyCountry=input("Enter the name of your country: ")
Length=len(MyCountry)
FirstThree=MyCountry[:3]
LastFive=MyCountry[-5:]
UpperCase=MyCountry .upper()
LowerCase=MyCountry .lower()
print("The length of the country name is", Length)
print("The first three characters of the country name are", FirstThree)
print("The last five characters of the country name are", LastFive)
print("The country name in upper case is", UpperCase)
print("The country name in lower case is", LowerCase)